# 카테고리명 키워드 우선순위 (긴 키워드가 먼저 매칭)
_SORTED_KEYS = sorted(WORKFLOW_TEMPLATES.keys(), key=len, reverse=True)


# ─────────────────────────────────────────────────────────────────────────────
# 파일 기반 업무흐름 추론
# ─────────────────────────────────────────────────────────────────────────────

def infer_workflow_from_files(
    related_files: list[str],
    max_steps: int = 7,
    min_steps: int = 2,
) -> list[str]:
    """파일명 키워드에서 업무 단계를 자동 추론한다.

    동작 원리:
      1. 각 파일의 stem(소문자)을 _WORKFLOW_HINTS 키워드와 비교
      2. 감지된 단계를 order 기준으로 정렬
      3. 중복 단계명 제거 (동일 단계명은 최초 1회만 유지)
      4. max_steps 개 이내로 반환

    Args:
        related_files: 파일 경로 목록
        max_steps: 반환할 최대 단계 수
        min_steps: 이 수보다 적으면 빈 리스트 반환 (fallback 유도)

    Returns:
        단계명 목록 (순서 보장). 단계가 min_steps 미만이면 빈 리스트.
    """
    import re

    detected: dict[str, int] = {}   # step_name → order
    for fpath in related_files[:50]:
        # 마지막 경로 컴포넌트 = 파일명
        fname = re.split(r"[/\\]", fpath.replace("\\", "/"))[-1]
        stem  = re.sub(r"\.[^.]+$", "", fname).lower()

        for kw, (step_name, order) in _WORKFLOW_HINTS.items():
            if kw.lower() in stem:
                # 동일 단계명이 아직 없거나, 더 앞 순서면 갱신
                if step_name not in detected or order < detected[step_name]:
                    detected[step_name] = order

    if len(detected) < min_steps:
        return []


    # order 오름차순 정렬 → 최대 max_steps 개
    steps = sorted(detected.items(), key=lambda x: x[1])
    return [name for name, _ in steps[:max_steps]]


# ─────────────────────────────────────────────────────────────────────────────
# 공개 API
# ─────────────────────────────────────────────────────────────────────────────

def get_workflow(category_name: str) -> list[str] | None:
    """카테고리명에 맞는 템플릿 업무 흐름 단계 리스트를 반환한다 (fallback 용).

    Returns:
        단계 목록 또는 None
    """
    cat_lower = category_name.lower()
    for key in _SORTED_KEYS:
        if key.lower() in cat_lower:
            return WORKFLOW_TEMPLATES[key]
    return None


def render_workflow(
    category_name: str,
    related_files: list[str] | None = None,
) -> str:
    """업무 흐름을 화살표(↓) 형식의 문자열로 렌더링한다.

    우선순위:
      1. 파일 기반 자동 추론 (related_files 제공 시)
      2. 카테고리명 기반 템플릿 (fallback)
      3. 최종 fallback — 기타 템플릿

    Args:
        category_name: 업무 카테고리명
        related_files : 카테고리 내 모든 프로젝트의 관련 파일 경로 목록 (선택)

    Returns:
        "단계1\n↓\n단계2\n..." 형식 문자열
    """
    source_label = ""

    # ① 파일 기반 자동 추론
    steps: list[str] | None = None
    if related_files:
        inferred = infer_workflow_from_files(related_files)
        if inferred:
            steps = inferred
            source_label = "*(산출물 기반 자동 추론)*"

    # ② 카테고리명 템플릿 fallback
    if not steps:
        steps = get_workflow(category_name)
        if steps:
            source_label = "*(카테고리 템플릿)*"

    # ③ 최종 fallback
    if not steps:
        steps = WORKFLOW_TEMPLATES.get("기타", [])
        source_label = "*(기본 흐름)*"

    lines: list[str] = []
    if source_label:
        lines.append(source_label)
        lines.append("")
    for i, step in enumerate(steps):
        lines.append(step)
        if i < len(steps) - 1:
            lines.append("↓")
    return "\n".join(lines)
